/**
 * Builds a full standalone SVG document for the given icon, recolored to
 * `colorHex` (no leading '#'), ready for rasterization.
 */
export function buildIconSvg(icon: IconKey, colorHex: string, strokeWidth = 2): string {
  const inner = ICON_INNER_MARKUP[icon];
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="#${colorHex}" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round">${inner}</svg>`;
}
